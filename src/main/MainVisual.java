    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        if (fila > 0) {
            int id = (Integer) jTable1.getModel().getValueAt(fila, 0);
            new personaEditar(this, true, id).setVisible(true);
            List a = new IntermedirioPersona().ListarPersonas();
            GestorTabla.rellena(a, modelo);
            jTable1.setModel(modelo);
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar una fila de la tabla", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        if (fila > 0) {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar esta persona?", "Warning",JOptionPane.OK_CANCEL_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                int id = (Integer) jTable1.getModel().getValueAt(fila, 0);
                boolean status = new IntermedirioPersona().Eliminar(new IntermedirioPersona().Buscar(id));
                if (status) {
                    List a = new IntermedirioPersona().ListarPersonas();
                    GestorTabla.rellena(a, modelo);
                    jTable1.setModel(modelo);
                    JOptionPane.showMessageDialog(null, "Persona eliminada de la base de datos", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Ocurrio un error al realizar la eliminacion de la persona seleccionada", "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar una fila de la tabla", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

